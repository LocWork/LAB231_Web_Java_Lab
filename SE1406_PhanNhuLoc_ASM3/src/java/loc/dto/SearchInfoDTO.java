package loc.dto;

/**
 *
 * @author hi
 */
public class SearchInfoDTO {

    private String hotelID, hotelName, hotelDescription, hotelImage, areaName;
    private int roomAmount;

    public SearchInfoDTO() {
    }

    public SearchInfoDTO(String hotelID, String hotelName, String hotelDescription, String hotelImage, String areaName, int roomAmount) {
        this.hotelID = hotelID;
        this.hotelName = hotelName;
        this.hotelDescription = hotelDescription;
        this.hotelImage = hotelImage;
        this.areaName = areaName;
        this.roomAmount = roomAmount;
    }

    public String getHotelID() {
        return hotelID;
    }

    public void setHotelID(String hotelID) {
        this.hotelID = hotelID;
    }

    public String getHotelName() {
        return hotelName;
    }

    public void setHotelName(String hotelName) {
        this.hotelName = hotelName;
    }

    public String getHotelDescription() {
        return hotelDescription;
    }

    public void setHotelDescription(String hotelDescription) {
        this.hotelDescription = hotelDescription;
    }

    public String getHotelImage() {
        return hotelImage;
    }

    public void setHotelImage(String hotelImage) {
        this.hotelImage = hotelImage;
    }

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }

    public int getRoomAmount() {
        return roomAmount;
    }

    public void setRoomAmount(int roomAmount) {
        this.roomAmount = roomAmount;
    }

}
