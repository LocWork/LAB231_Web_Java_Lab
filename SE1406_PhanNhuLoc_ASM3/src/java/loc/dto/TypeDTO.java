package loc.dto;

/**
 *
 * @author hi
 */
public class TypeDTO {

    private String typeID, name;

    public TypeDTO() {
    }

    public TypeDTO(String typeID, String name) {
        this.typeID = typeID;
        this.name = name;
    }

    public String getTypeID() {
        return typeID;
    }

    public void setTypeID(String typeID) {
        this.typeID = typeID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
