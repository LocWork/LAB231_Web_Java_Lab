package loc.dto;

/**
 *
 * @author hi
 */
public class AreaDTO {

    private String areaID, name;

    AreaDTO() {
    }

    public AreaDTO(String areaID, String name) {
        this.areaID = areaID;
        this.name = name;
    }

    public String getAreaID() {
        return areaID;
    }

    public void setAreaID(String areaID) {
        this.areaID = areaID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
